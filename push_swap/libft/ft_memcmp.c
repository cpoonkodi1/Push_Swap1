#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t len)
{
	unsigned char	*ptr1;
	unsigned char	*ptr2;

	ptr1 = (unsigned char *)s1;
	ptr2 = (unsigned char *)s2;
	while (len--)
	{
		if (*ptr1++ != *ptr2++)
			return ((*--ptr1) - (*--ptr2));
	}
	return (0);
}
